package com.devmons.entity;

/**
 * Enum representing issue priority levels.
 * 
 * LOW: Low priority, can be done later
 * MEDIUM: Normal priority, should be done soon
 * HIGH: High priority, should be done ASAP
 * CRITICAL: Critical priority, urgent fix required
 */
public enum Priority {
    LOW,
    MEDIUM,
    HIGH,
    CRITICAL
}

